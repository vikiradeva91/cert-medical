					<li>
						Classification of the medical device (Annex VIII of Regulation 2017/745) and
						documentation of the results of the classification;
					</li>
					<li>
						Analysis and identification of applicable safety and performance
						requirements and identification of compliance measures (Annex I of
						Regulation 745);
					</li>
					<li>
						Risk management process integration in accordance with EN ISO 14971. Risk
						evaluation planning and reporting;
					</li>
					<li>
						Determination of applicable harmonised standards and technical
						specification;
					</li>
					<li>
						Design / development of the medical device (development of the necessary
						documents describing the composition / construction of the device),
						verification and validation of the development of the device (documentation
						of the process and its results);
					</li>
					<li>
						Clinical evaluation process integration. Clinical evaluation planning and
						reporting;
					</li>
					<li>
						Product testing organisation. Accredited laboratories research, selection
